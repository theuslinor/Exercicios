print('\033[0;33mÉ alfanúmerico?\033[m ', a.isalnum())
print('\033[35mEstá em maiúsculo?\033[m ', a.isupper())
print('\033[36mEstá em minúsculo?\033[m ', a.islower())
print('\033[1;32;40mEstá capitalizada?\033[m ', a.istitle())